    private boolean varifyid(){
        int ln = jPasswordField1.getPassword().length;
        if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jPasswordField1.getPassword().equals("") || jPasswordField2.getPassword().equals("") || !jRadioButton1.isSelected() && !jRadioButton2.isSelected()){
            JOptionPane.showMessageDialog(null,"One or more field empty!!");
            return false;
        }else if(!String.valueOf(jPasswordField1.getPassword()).equals(String.valueOf(jPasswordField2.getPassword()))){
            JOptionPane.showMessageDialog(null,"Password not match... please try again");
            return false;
        }else if(ln<4){
            JOptionPane.showMessageDialog(null,"Password too small");
            return false;
        }else{
            return true;
        }
    }
    
    private boolean isUserexist(String st){
        Boolean isuser = false;
        Connection con = MyConnection.getConnection();
        PreparedStatement ps =null;
        ResultSet rs = null;
        
        try {
            ps = con.prepareStatement("SELECT * FROM `user1` WHERE `sid` =?");
            ps.setString(1, st);
            rs = ps.executeQuery();
            if(rs.next()){
                isuser = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return isuser;
    }
    
