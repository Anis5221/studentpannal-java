    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(jRadioButton1.isSelected()){
            gander = "Male";
        }else if(jRadioButton2.isSelected()){
            gander = "Female";
        }
        if(varifyid()){
            Connection con = MyConnection.getConnection();
        PreparedStatement ps = null;
        try {
           
            ps = con.prepareStatement("INSERT INTO `user1`(`name`, `sid`, `mail`, `phone`, `gander`, `pass`, `photo`) VALUES (?,?,?,?,?,?,?)");
            InputStream is = new FileInputStream(new File(s));
            ps.setString(1, jTextField1.getText());
            ps.setInt(2, Integer.valueOf(jTextField2.getText()));
            ps.setString(3, jTextField3.getText());
            ps.setInt(4, Integer.valueOf(jTextField4.getText()));
            ps.setString(5, gander);
            ps.setString(6, String.valueOf(jPasswordField1.getPassword()));
            ps.setBlob(7, is);
            
            
            if(isUserexist(jTextField2.getText())){
                JOptionPane.showMessageDialog(null, "Already have an Acount!!");
            }else{
                if(ps.executeUpdate() != 0){
                    JOptionPane.showMessageDialog(null, "Acount Created");
                    LogIn lg = new LogIn();
                    lg.setVisible(true);
                    lg.setLocationRelativeTo(null);
                    lg.pack();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Something Wrong");
                }
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (FileNotFoundException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

