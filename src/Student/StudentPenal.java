    public void getData(int n){
        FachingDatato f = new FachingDatato();
        ResultSet rs ;
        rs = f.Find(n);
        try {
            if(rs.next()){
                name = rs.getString("name");
                rol = rs.getInt("sid");
                photo = String.valueOf(rs.getBlob("photo"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(StudentPenal.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }
